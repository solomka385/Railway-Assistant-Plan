def get_subdivisions_prompt(
    context_users: str,
    examples_text: str,
    subdivisions_examples_context: str,
    formatted_history: str,
    prev_subdiv_str: str,
    question: str,
    allowed_subdivisions_str: str,
    is_edit: bool = False,
    edit_type: str = ""
) -> str:
    """
    Формирует промпт для определения необходимых подразделений.
    
    Args:
        context_users: Контекст из документов о подразделениях
        examples_text: Примеры аварий
        subdivisions_examples_context: Примеры подразделений и аварий
        formatted_history: История диалога
        prev_subdiv_str: Предыдущий список подразделений
        question: Текущий вопрос
        allowed_subdivisions_str: Список допустимых подразделений
        is_edit: Является ли запрос правкой
        edit_type: Тип правки (add/remove/replace/general)
    
    Returns:
        Сформированный промпт
    """
    # Инструкции для правок
    edit_instructions = ""
    edit_examples = ""
    if is_edit and prev_subdiv_str:
        if edit_type == "add":
            edit_instructions = """
КРИТИЧЕСКИ ВАЖНО: Это запрос на ДОБАВЛЕНИЕ подразделений к существующему списку.
1. ПРЕДЫДУЩИЙ СПИСОК ПОДРАЗДЕЛЕНИЙ ДОЛЖЕН БЫТЬ СОХРАНЕН ПОЛНОСТЬЮ!
2. Добавьте только запрошенные подразделения к предыдущему списку.
3. НЕ УДАЛЯЙТЕ и НЕ ИЗМЕНЯЙТЕ существующие подразделения из предыдущего списка.
4. Объедините предыдущий список с новыми подразделениями.
"""
            edit_examples = """
ПРИМЕР ПРАВКИ (ДОБАВЛЕНИЕ):
Предыдущий список подразделений: ДАВС, ЦДИ
Вопрос: Добавь РСЧС
Ответ:
1. ДАВС - дирекция аварийно-восстановительных средств
2. ЦДИ - Центральная дирекция инфраструктуры
3. РСЧС - Единая государственная система предупреждения ЧС
"""
        elif edit_type == "remove":
            edit_instructions = """
КРИТИЧЕСКИ ВАЖНО: Это запрос на УДАЛЕНИЕ подразделений из существующего списка.
1. Используйте предыдущий список подразделений как основу.
2. Удалите только запрошенные подразделения.
3. Сохраните ВСЕ остальные подразделения из предыдущего списка.
4. НЕ ДОБАВЛЯЙТЕ новые подразделения, если они не запрашивались.
"""
            edit_examples = """
ПРИМЕР ПРАВКИ (УДАЛЕНИЕ):
Предыдущий список подразделений: ДАВС, ЦДИ, РСЧС
Вопрос: Удали РСЧС
Ответ:
1. ДАВС - дирекция аварийно-восстановительных средств
2. ЦДИ - Центральная дирекция инфраструктуры
"""
        elif edit_type == "replace":
            edit_instructions = """
КРИТИЧЕСКИ ВАЖНО: Это запрос на ЗАМЕНУ подразделений в существующем списке.
1. Замените только указанные подразделения на новые.
2. Сохраните ВСЕ остальные подразделения из предыдущего списка без изменений.
"""
            edit_examples = """
ПРИМЕР ПРАВКИ (ЗАМЕНА):
Предыдущий список подразделений: ДАВС, ЦДИ
Вопрос: Замени ЦДИ на ЦЧС
Ответ:
1. ДАВС - дирекция аварийно-восстановительных средств
2. ЦЧС - Ситуационный центр мониторинга ЧС
"""
        else:
            edit_instructions = """
КРИТИЧЕСКИ ВАЖНО: Это запрос на ИЗМЕНЕНИЕ списка подразделений.
1. Используйте предыдущий список как основу.
2. Внесите запрошенные изменения.
3. Сохраните ВСЕ подразделения, которые не должны быть изменены.
4. НЕ УДАЛЯЙТЕ подразделения без явного запроса.
"""

    prompt = f"""<|im_start|>system
{GROUNDED_SYSTEM_PROMPT}

ВАЖНО: ВОЗВРАЩАЙ ТОЛЬКО ОТВЕТ В ТРЕБУЕМОМ ФОРМАТЕ БЕЗ ЛИШНИХ КОММЕНТАРИЕВ!

ИНСТРУКЦИИ:
1. Определи НЕОБХОДИМЫЕ подразделения для решения ситуации из вопроса.
2. Выведи в формате: 1. КОД - полное название
3. ИСПОЛЬЗУЙ ТОЛЬКО подразделения из списка ниже.
{edit_instructions}

СПИСОК ДОПУСТИМЫХ ПОДРАЗДЕЛЕНИЙ:
{allowed_subdivisions_str}

ПРИМЕР:
Вопрос: Обрушение моста
Ответ:
1. ДАВС - дирекция аварийно-восстановительных средств
2. ЦДИ - Центральная дирекция инфраструктуры
3. РСЧС - Единая государственная система предупреждения ЧС
{edit_examples}
<|im_end|>
<|im_start|>user
Контекст:
{context_users}

История:
{formatted_history}

Предыдущий список: {prev_subdiv_str}

Вопрос: {question}<|im_end|>
<|im_start|>assistant
"""
    return prompt


