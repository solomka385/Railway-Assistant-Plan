def get_chat_prompt(
    context: str,
    formatted_history: str,
    question: str
) -> str:
    """
    Формирует промпт для режима чата.
    
    Args:
        context: Контекст из документов
        formatted_history: История диалога
        question: Текущий вопрос
    
    Returns:
        Сформированный промпт
    """
    prompt = CHAT_SYSTEM_PROMPT
    if context and context != "Нет информации о подразделениях":
        prompt += f"<|im_start|>user\nКонтекст из документов:\n{context}\n\n"
    prompt += f"История диалога:\n{formatted_history}\n\nВопрос: {question}<|im_end|>\n<|im_start|>assistant\n"
    return prompt
