def format_technic_reference_for_prompt() -> str:
    """
    Форматирует справочник техники для вставки в промпт.
    Возвращает строку с названиями техники, сгруппированными по подразделениям.
    """
    if not app_state.technic_reference:
        return "Справочник техники не загружен."
    
    # Группируем по подразделениям
    grouped = {}
    for item in app_state.technic_reference:
        subdivision = item.get('подразделение', 'Другие')
        name = item.get('название_техники', '')
        if name:
            if subdivision not in grouped:
                grouped[subdivision] = []
            grouped[subdivision].append(name)
    
    # Формируем текст - ограничиваем до 5 на подразделение для сокращения промпта
    lines = []
    for subdivision in sorted(grouped.keys()):
        names = sorted(grouped[subdivision])[:5]
        lines.append(f"{subdivision}: {', '.join(names)}")
    
    return "\n".join(lines)


